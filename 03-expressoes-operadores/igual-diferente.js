let one = 1;
let two = 2;

// == igual a
console.log(`
  ${one} é igual a ${two}? ${one == two} 
  ${one} é igual a ${one}? ${one == one} 
  ${one} é igual a "1"? ${one == "1"} 
`);

// != diferente de
console.log(`
  ${one} é diferente de ${two}? ${one != two} 
  ${one} é diferente de ${one}? ${one != one} 
  ${one} é diferente de "1"? ${one != "1"} 
`);